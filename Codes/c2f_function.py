def c2f(celsius):
    f = (celsius * 1.8) + 32 
    return f
    

c = 0
f = c2f(c)
print(f'{c} degrees Celsius are {f} degrees Fahrenheit')

c = 10
f = c2f(c)
print(f'{c} degrees Celsius are {f} degrees Fahrenheit')

c = 20
f = c2f(c)
